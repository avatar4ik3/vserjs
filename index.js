function rotate_image() {
    let element = document.getElementById('img')
    if (element.classList.contains('img-anim') == false) {
        define_image_class(element)
    }
    element.className = 'img-anim'
}

function define_image_class(element) {
    element.classList.add('img-anim')
}
function find_prediction() {
    if (id_state() == true) {
        fill_local_storage()
    }
    let id = localStorage.getItem("id")
    document.getElementById("inner").innerText = predictions[id]
}



function fill_local_storage() {
    let id = Math.floor(Math.random() * (predictions.length))
    localStorage.setItem("id", id)
}

function on_image_click() {
    if (state != 1) {
        rotate_image()
        find_prediction()
        state = 1
    }
}
